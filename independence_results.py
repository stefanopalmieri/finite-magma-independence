def print_table(table, n, label=""):
    if label:
        print(f"\n  {label}")
    header = "     " + "".join(f"{j:3d}" for j in range(n))
    print(header)
    print("     " + "---" * n)
    for i in range(n):
        row_str = "".join(f"{table[i][j]:3d}" for j in range(n))
        print(f"  {i:2d} |{row_str}")


