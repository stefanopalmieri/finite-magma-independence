def gen_s_not_d(n=8):
    """S ⊬ D: FRM with retraction pair but violating Kripke dichotomy."""
    s, dot = base_solver(n)
    # Retraction pair: Q=2, E=3
    for x in range(2, n):
        qx = dot[2][x]
        s.add(col_ite_lookup(dot, 3, qx, n) == x)
        ex = dot[3][x]
        s.add(col_ite_lookup(dot, 2, ex, n) == x)
    s.add(dot[3][0] == 0)
    s.add(dot[3][1] == 1)
    # Classifier at index 4
    for j in range(n):
        s.add(Or(dot[4][j] == 0, dot[4][j] == 1))
    # Require at least one mixed element
    mixed_clauses = []
    for x in range(2, n):
        has_bool = Or([Or(dot[x][j] == 0, dot[x][j] == 1) for j in range(2, n)])
        has_nonbool = Or([dot[x][j] >= 2 for j in range(2, n)])
        mixed_clauses.append(And(has_bool, has_nonbool))
    s.add(Or(mixed_clauses))
    return s, dot, n


def gen_d_not_h_compose(n=10):
    """D ⊬ H (Compose): DRM with Kripke but no Compose element."""
    s, dot = base_solver(n)
    # Retraction pair: Q=2, E=3
    for x in range(2, n):
        qx = dot[2][x]
        s.add(col_ite_lookup(dot, 3, qx, n) == x)
        ex = dot[3][x]
        s.add(col_ite_lookup(dot, 2, ex, n) == x)
    s.add(dot[3][0] == 0)
    s.add(dot[3][1] == 1)
    # Classifier at index 4
    for j in range(n):
        s.add(Or(dot[4][j] == 0, dot[4][j] == 1))
    # Kripke dichotomy
    for x in range(2, n):
        is_tst = And([Or(dot[x][j] == 0, dot[x][j] == 1) for j in range(n)])
        for y in range(2, n):
            s.add(Or(is_tst, dot[x][y] >= 2))
    # Branch: τ=4, f=5, g=6, ρ=7
    for x in range(2, n):
        s.add(If(dot[4][x] == 0, dot[7][x] == dot[5][x], dot[7][x] == dot[6][x]))
    s.add(Or([dot[5][j] != dot[6][j] for j in range(2, n)]))
    # NO element satisfies Compose: η·x = ρ·(g·x) for all x in core
    for eta_i in range(2, n):
        s.add(Or([dot[eta_i][x] != col_ite_lookup(dot, 7, dot[6][x], n) for x in range(2, n)]))
    return s, dot, n


def gen_d_not_h_inert(n=10):
    """D ⊬ H (Inert): DRM with Kripke but no inert element."""
    s, dot = base_solver(n)
    # Retraction pair: Q=2, E=3
    for x in range(2, n):
        qx = dot[2][x]
        s.add(col_ite_lookup(dot, 3, qx, n) == x)
        ex = dot[3][x]
        s.add(col_ite_lookup(dot, 2, ex, n) == x)
    s.add(dot[3][0] == 0)
    s.add(dot[3][1] == 1)
    # Classifier at index 4
    for j in range(n):
        s.add(Or(dot[4][j] == 0, dot[4][j] == 1))
    # Kripke dichotomy
    for x in range(2, n):
        is_tst = And([Or(dot[x][j] == 0, dot[x][j] == 1) for j in range(n)])
        for y in range(2, n):
            s.add(Or(is_tst, dot[x][y] >= 2))
    # Every non-absorber is tester or encoder (no inert)
    for x in range(2, n):
        is_tst = And([Or(dot[x][j] == 0, dot[x][j] == 1) for j in range(n)])
        has_2_distinct_nonbool = []
        for a in range(2, n):
            for b in range(a + 1, n):
                has_2_distinct_nonbool.append(And(dot[x][a] >= 2, dot[x][b] >= 2, dot[x][a] != dot[x][b]))
        is_enc = Or(has_2_distinct_nonbool) if has_2_distinct_nonbool else False
        s.add(Or(is_tst, is_enc))
    return s, dot, n


def gen_h_not_d(n=10):
    """H ⊬ D: FRM with Branch+Compose+Y but violating Kripke."""
    s, dot = base_solver(n)
    Q, E, TAU, F, G, RHO, ETA, Y = 2, 3, 4, 5, 6, 7, 8, 9
    # Retraction pair
    for x in range(2, n):
        qx = dot[Q][x]
        s.add(col_ite_lookup(dot, E, qx, n) == x)
        ex = dot[E][x]
        s.add(col_ite_lookup(dot, Q, ex, n) == x)
    s.add(dot[E][0] == 0)
    s.add(dot[E][1] == 1)
    # Classifier
    for j in range(n):
        s.add(Or(dot[TAU][j] == 0, dot[TAU][j] == 1))
    # Branch
    for x in range(2, n):
        s.add(If(dot[TAU][x] == 0, dot[RHO][x] == dot[F][x], dot[RHO][x] == dot[G][x]))
    s.add(Or([dot[F][j] != dot[G][j] for j in range(2, n)]))
    # Inert
    for x in range(2, n):
        s.add(dot[G][x] >= 2)
    # Compose
    for x in range(2, n):
        gx = dot[G][x]
        s.add(dot[ETA][x] == col_ite_lookup(dot, RHO, gx, n))
    # Y fixed-point
    y_rho = dot[Y][RHO]
    s.add(y_rho == col_ite_lookup(dot, RHO, y_rho, n))
    s.add(y_rho >= 2)
    # Require at least one mixed element (violates Kripke)
    mixed_clauses = []
    for x in range(2, n):
        has_bool = Or([Or(dot[x][j] == 0, dot[x][j] == 1) for j in range(2, n)])
        has_nonbool = Or([dot[x][j] >= 2 for j in range(2, n)])
        mixed_clauses.append(And(has_bool, has_nonbool))
    s.add(Or(mixed_clauses))
    return s, dot, n


