#!/usr/bin/env python3
"""
Independence Results for the Three Capabilities (S, D, H)

Generates, verifies, and displays the four canonical counterexamples
that establish full independence of the three capabilities:

  S ⊬ D  (the Countermodel, N=8): self-simulates, violates Kripke
  D ⊬ H  (N=10, Test D):                 has Kripke, no Compose element
  D ⊬ H  (N=10, Test E):                 has Kripke, no Inert element
  H ⊬ D  (N=10, diagonal):               has Branch+Compose+Y, violates Kripke

Each counterexample is:
  1. Generated by Z3 SAT solver
  2. Verified against all claimed properties (independent of Z3)
  3. Displayed with full Cayley table and property summary

Run:  python3 independence_results.py
"""

from __future__ import annotations
import json
import sys
import time
from pathlib import Path
from z3 import And, If, Int, Not, Or, Solver, sat, unsat


# ═══════════════════════════════════════════════════════════════════════
# SAT Helpers
# ═══════════════════════════════════════════════════════════════════════

def col_ite_lookup(dot, row, col_expr, n):
    entry = dot[row][0]
    for c in range(1, n):
        entry = If(col_expr == c, dot[row][c], entry)
    return entry


def base_solver(n, timeout=300):
    s = Solver()
    s.set("timeout", timeout * 1000)
    dot = [[Int(f"d_{i}_{j}") for j in range(n)] for i in range(n)]
    for i in range(n):
        for j in range(n):
            s.add(dot[i][j] >= 0, dot[i][j] < n)
    for j in range(n):
        s.add(dot[0][j] == 0)
        s.add(dot[1][j] == 1)
    for x in range(2, n):
        s.add(Or([dot[x][j] != x for j in range(n)]))
    for x in range(n):
        for y in range(x + 1, n):
            s.add(Or([dot[x][j] != dot[y][j] for j in range(n)]))
    return s, dot


def extract_table(s, dot, n):
    m = s.model()
    return [[m.evaluate(dot[i][j]).as_long() for j in range(n)] for i in range(n)]


# ═══════════════════════════════════════════════════════════════════════
# Verification (independent of Z3 — operates on plain tables)
# ═══════════════════════════════════════════════════════════════════════

def verify_absorbers(table, n):
    """Check indices 0 and 1 are left-absorbers."""
    ok = True
    for j in range(n):
        if table[0][j] != 0: ok = False
        if table[1][j] != 1: ok = False
    return ok


def verify_no_extra_absorbers(table, n):
    """No element other than 0,1 is a left-absorber."""
    for x in range(2, n):
        if all(table[x][j] == x for j in range(n)):
            return False
    return True


def verify_extensionality(table, n):
    """All rows are distinct."""
    for x in range(n):
        for y in range(x + 1, n):
            if table[x] == table[y]:
                return False
    return True


def verify_retraction(table, n, q, e):
    """E(Q(x)) = x and Q(E(x)) = x for all x in core."""
    for x in range(2, n):
        qx = table[q][x]
        if qx < 0 or qx >= n: return False
        if table[e][qx] != x: return False
        ex = table[e][x]
        if ex < 0 or ex >= n: return False
        if table[q][ex] != x: return False
    return True


def verify_e_transparency(table, e):
    """E(⊤) = ⊤ and E(⊥) = ⊥."""
    return table[e][0] == 0 and table[e][1] == 1


def verify_classifier(table, n, tau):
    """τ has all-boolean outputs."""
    return all(table[tau][j] in (0, 1) for j in range(n))


def verify_kripke(table, n):
    """Every non-absorber is either all-boolean or all-non-boolean on core."""
    for x in range(2, n):
        core_vals = [table[x][j] for j in range(2, n)]
        all_bool = all(v in (0, 1) for v in core_vals)
        all_nonbool = all(v >= 2 for v in core_vals)
        if not (all_bool or all_nonbool):
            return False
    return True


def find_mixed_elements(table, n):
    """Find elements with mixed boolean/non-boolean outputs on core."""
    mixed = []
    for x in range(2, n):
        core_vals = [table[x][j] for j in range(2, n)]
        has_bool = any(v in (0, 1) for v in core_vals)
        has_nonbool = any(v >= 2 for v in core_vals)
        if has_bool and has_nonbool:
            mixed.append(x)
    return mixed


def verify_branch(table, n, tau, f, g, rho):
    """ρ·x = f·x if τ·x=⊤, else ρ·x = g·x, on core."""
    for x in range(2, n):
        if table[tau][x] == 0:
            if table[rho][x] != table[f][x]: return False
        else:
            if table[rho][x] != table[g][x]: return False
    return True


def verify_compose(table, n, rho, g, eta):
    """η·x = ρ·(g·x) on core."""
    for x in range(2, n):
        gx = table[g][x]
        if gx < 0 or gx >= n: return False
        if table[eta][x] != table[rho][gx]: return False
    return True


def verify_inert(table, n, g):
    """g maps core away from absorbers."""
    for x in range(2, n):
        if table[g][x] < 2: return False
    return True


def verify_y_fixpoint(table, rho, y):
    """Y·ρ = ρ·(Y·ρ) and Y·ρ ≥ 2."""
    yr = table[y][rho]
    n = len(table)
    if yr < 2 or yr >= n: return False
    return table[rho][yr] == yr


def classify_elements(table, n):
    """Classify non-absorber elements into testers, encoders, mixed."""
    testers, encoders, mixed = [], [], []
    for x in range(2, n):
        core_vals = [table[x][j] for j in range(2, n)]
        all_bool = all(v in (0, 1) for v in core_vals)
        all_nonbool = all(v >= 2 for v in core_vals)
        if all_bool:
            testers.append(x)
        elif all_nonbool:
            encoders.append(x)
        else:
            mixed.append(x)
    return testers, encoders, mixed


